package com.example.english_app.colleges.reading;

public class ReadingStaticRcvModel {

    private final int image;
    private final String text;

    public ReadingStaticRcvModel(int image, String text) {
        this.image = image;
        this.text = text;
    }

    public int getImage() {
        return image;
    }

    public String getText() {
        return text;
    }

}
